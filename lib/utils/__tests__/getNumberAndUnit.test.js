'use strict';

const getNumberAndUnit = require('../getNumberAndUnit');
const valueParser = require('postcss-value-parser');

it('getNumberAndUnit', () => {
	expect(getNumberAndUnit()).toBeNull();
	expect(getNumberAndUnit({})).toBeNull();
	expect(getNumberAndUnit(valueParser('1.1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('1.1000s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('1.1S').nodes[0]).unit).toBe('S');
	expect(getNumberAndUnit(valueParser('+1.1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+1.1000s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-1.1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-1.1000s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('1.1e10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+1.1e10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-1.1e10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('1.1e+10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('1.1e-10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+1.1e+10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+1.1e-10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-1.1e-10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-1.1e+10s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('1pX').nodes[0]).unit).toBe('pX');
	expect(getNumberAndUnit(valueParser('1PX').nodes[0]).unit).toBe('PX');
	expect(getNumberAndUnit(valueParser('+1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('-1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('1e1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('+1e1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('-1e1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('1e-1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('1e+1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('+1e+1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('+1e-1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('-1e-1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('-1e+1px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('.1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+.1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-.1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+.1000s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-.1000s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('.1e1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+.1e1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-.1e1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('.1e-1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('.1e+1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+.1e+1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('+.1e-1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-.1e-1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('-.1e+1s').nodes[0]).unit).toBe('s');
	expect(getNumberAndUnit(valueParser('100%').nodes[0]).unit).toBe('%');
	expect(getNumberAndUnit(valueParser('100').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('0\\0').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('10px\\9').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('6e-2px').nodes[0]).unit).toBe('px');
	expect(getNumberAndUnit(valueParser('.0').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('+.0').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('-.0').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('.0e1').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('+.0e1').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('-.0e1').nodes[0]).unit).toBe('');
	expect(getNumberAndUnit(valueParser('#fff').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('#000').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('#zzzzzz').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('#F').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('#PX').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('"100"').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('"100px"').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser(' ').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('/').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('+').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('word').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('px').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('url()').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('url()px').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('$variable').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('${$variable}').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('${$variable}px').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('${$variable}px').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('@variable').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('e1').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('wordPX').nodes[0])).toBeNull();
	expect(getNumberAndUnit(valueParser('..0').nodes[0])).toBeNull();
});
